let day: number = 2

switch (day) {
    case 0:
        console.log("Pazartesi")
        break;
    case 1:
        console.log("Salı")
        break;
    case 2:
        console.log("Çarşamba")
        break;
    case 3:
        console.log("Perşembe")
        break;
    case 4:
        console.log("Cuma")
        break;
    case 5:
        console.log("Cumartesi")
        break;
    default:
        console.log("Pazar")
        break;

}